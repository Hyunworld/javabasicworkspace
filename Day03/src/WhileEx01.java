
public class WhileEx01 {
	public static void main(String[] args) {
		
		int a = 1; //제어변수 : 반복문의 횟수를 제어할 변수
		int sum= 0;
		
		while(a  <= 10) {
			
			sum +=  a;
			
			a++; //제어변수의 조작을 통해서 조건이 언젠가 false가 되게 처리
			
			System.out.println(sum);
		}
		

		
		//1~10까지 합?

		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
